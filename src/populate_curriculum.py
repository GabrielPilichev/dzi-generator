def populate(vault: Path) -> None:
    if not vault.exists():
        print(f"❌ Vault не съществува: {vault}")
        return
    
    mocs_dir = vault / "MOCs"
    topics_dir = vault / "Topics"
    if not mocs_dir.exists():
        print(f"❌ MOCs/ не съществува. Пусни setup_vault.py първо.")
        return
    
    print(f"📂 Vault: {vault}")
    
    # 1. Backup
    backup_dir = vault / "_backup_initial_mocs"
    if not backup_dir.exists():
        backup_dir.mkdir()
        print(f"\n📦 Backup на текущите MOCs в _backup_initial_mocs/...")
        for f in mocs_dir.glob("*.md"):
            shutil.copy2(f, backup_dir / f.name)
            print(f"   ✓ {f.name}")
        # Backup home too
        home = vault / "Home.md"
        if home.exists():
            shutil.copy2(home, backup_dir / "Home.md")
            print(f"   ✓ Home.md")
    else:
        print(f"\n⏭️  Backup вече съществува (_backup_initial_mocs/), не го презаписвам")
    
    # 2. Write Home
    print(f"\n📄 Home.md...")
    (vault / "Home.md").write_text(
        HOME_MD.format(date=datetime.now().strftime("%Y-%m-%d")),
        encoding="utf-8"
    )
    print(f"   ✓ Updated")
    
    # 3. Write all MOCs
    print(f"\n📄 MOCs ({len(MOC_FILES)} files)...")
    for fname, content in MOC_FILES.items():
        (mocs_dir / fname).write_text(content, encoding="utf-8")
        print(f"   ✓ {fname}")
    
    # 4. Topic stubs (idempotent — не презаписва ако съществува)
    topics_dir.mkdir(exist_ok=True)
    created = 0
    skipped = 0
    print(f"\n📄 Topic stubs ({len(TOPICS_TO_CREATE)} concepts)...")
    for slug, title, parent, classes, tags, parent_link in TOPICS_TO_CREATE:
        path = topics_dir / f"{slug}.md"
        if path.exists():
            skipped += 1
            continue
        path.write_text(
            TOPIC_STUB.format(
                title=title,
                parent=parent,
                classes=classes,
                tags=tags,
                parent_link=parent_link,
            ),
            encoding="utf-8"
        )
        created += 1
    print(f"   ✓ Created: {created}, ⏭️  Skipped: {skipped}")
    
    print(f"\n✅ Готово!")
    print(f"\nВ Obsidian:")
    print(f"   1. Cmd+R — refresh vault-а")
    print(f"   2. Отвори Home.md — виж новите линкове за модулите")
    print(f"   3. Отвори graph view (Cmd+Shift+G) — погледни как се свързват MOCs и Topics")
    print(f"   4. Отвори някой topic stub (напр. Topics/sumif.md) и го развий")


def main() -> None:
    p = argparse.ArgumentParser()
    p.add_argument("--vault", type=Path,
                   default=Path.home() / "dzi-generator" / "vault")
    args = p.parse_args()
    populate(args.vault)


if __name__ == "__main__":
    main()
